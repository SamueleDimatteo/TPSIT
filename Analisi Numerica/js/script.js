function EsercizioN1() {

var factor = 1;
var countfactors = 0;
var numberfactorsmax;
var factorsmax = 0;

    for(var num = 1; num<1000; num++) {

        for(factor = 1; factor<=num/2; factor++) {

            if(num%factor == 0) {
                    
                countfactors++;

                if(countfactors>factorsmax) {

                    factorsmax = countfactors + 1;

                    numberfactorsmax = num;

                    document.getElementById("rispostaes1").innerHTML = numberfactorsmax;
                }
            }
        }

        countfactors = 0;

    }

    return numberfactorsmax;
           
}

